# Operadores de Comparação
a = 10
b = 5

# Igualdade
igual = a == b 
print(f"A é igual a B? {igual}")

# Diferença
diferente = a != b 
print(f"A é diferente de B? {diferente}")

# Maior que
maior_que = a > b  
print(f"A é maior que B? {maior_que}")

# Menor que
menor_que = a < b
print(f"A é menor que B? {menor_que}")

# Maior ou igual a
maior_igual = a >= b  
print(f"A é maior ou igual a B? {maior_igual}")
